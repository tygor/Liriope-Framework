  <a name="FAQ"></a>
  <h2>Baptism FAQ</h2>

  <dl class="question-answer">

  <dt class="rounded">1. What is baptism?</dt>
  <dd>Baptism is an act of obedience that illustrates Christ's death, burial, and resurrection. It also illustrates death to our old life and our new birth in Christ. Baptism doesn't save us; it tells people that Jesus is our Lord.</dd>

  <dt class="rounded">2. How does NRHC do baptism?</dt>
  <dd>NRHC practices baptism by immersion, because we believe it's most in line with the Scriptures.
  <ul>
  <li>Baptisms in the Bible were by immersion (Acts 8:38-39)</li>
  <li>The word transliterated "baptize" literally means "to dip under."</li>
  <li>Immersion best symbolizes a burial and resurrection</li>
  </ul>
  </dd>

  <dt class="rounded">3. What about my children?</dt>
  <dd>NRHC practices baby dedications for parents to express their desire to partner with the church in raising their children to know and love God. These baby dedications look forward to the day when the child makes their own decision to follow Christ as Lord and Savior, and the child (or adult) will then be baptized as a public profession of their own faith. </dd>

  <dt class="rounded">4. Where does NRHC baptize?</dt>
  <dd>We have several baptism events per year, one outdoor baptism in the spring/summer months, and others in Sunday services during the colder months.</dd>

  <dt class="rounded">5. What if I have been previously baptized?</dt>
  <dd>Baptism is a public profession of your faith in Christ. If your baptism was your decision and it accurately represented your new faith and life in Christ, then you have done what the scriptures ask. If you are not sure, we recommend you explore baptism as your next step</dd>

