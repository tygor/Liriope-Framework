<p>A church is far more than an event at the end of every week. And yet when you mention the word
&ldquo;church,&rdquo; this is what comes to mind. So, to keep things separate, we will describe our
weekend services on this page, and leave the rest of the website to describe what <span
class="strong">else</span> goes on with the people of NRHC.</p>

<h1>The Service</h1>
<p>North Rock Hill Church is a multi-service time church. We quickly learned that multiple service
times are a great way to create opportunity for more people to hear and see the Gospel. Please feel
free to try any of our service times to find the one that fits you best. See you on Sunday.</p>
<p>Services begin at:</p>
<ul>
  <li>8:30 AM</li>
  <li>10:00 AM</li>
  <li>11:30 AM</li>
</ul>

<h1>The Music</h1>
<p>One strong factor in a person&rqsuo;s search for a church family is the music style. There
are many terms and descriptors out there, so please give us some leeway. We are
&lsquo;contemporary&rsquo;. We have guitars, drums, and sometimes a dobro on stage.</p>

