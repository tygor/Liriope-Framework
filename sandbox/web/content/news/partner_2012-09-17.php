<h1>Monday, Sep 17<sup>th</sup>, 2012<br /> 6:00–8:30 pm</h1>
<p>So you've attended Discover class and plugged into a journey group. You're
supporting NRHC’s vision by serving and giving.  And now you're asking,
'What's next?' Glad you asked! The simple answer is partner. Why Partner
with NRHC? By becoming a Partner, you are joining the heart and soul of NRHC! The difference
between 'attenders' and Partners can be summed up in one word:
<b>COMMITMENT</b>. Partners can be counted on to help NRHC accomplish its vision!  </p>
<p>When you partner with NRHC, you are saying the following:</p>
<ol>
  <li>I have received Jesus as Savior and Lord. I have been transformed, realizing that I
  was dead in my sin, but Christ purchased my freedom through His death and resurrection. I
  am following Him as Lord.</li>
  <li>I have been baptized as a public profession of my faith, or I am scheduled to do so
  at the next baptism event.</li>
  <li>I am connected in community through a Journey Group.</li>
  <li>I am serving to help NRHC accomplish its vision.</li>
  <li>I am generously financially supporting the ministries of NRHC.</li>
</ol>
<p>Are you ready to Partner with North Rock Hill Church? Then join us for the
Partner class on Monday, Sep 17 from 6:00 PM to 8:30 PM.</p>
<p><strong><em>Dinner and childcare will be provided.</em></strong></p>

